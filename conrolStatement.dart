/*
*If-Else Statements: Used for conditional branching.
*Switch Case: Used for selecting one of many code blocks to execute.
*For Loop: Used for iterating over a range of values.
*While Loop: Used for repeating a block of code while a condition is true.
*Do-While Loop: Similar to while loop, but guarantees at least one iteration.
*Break: Exits from the loop or switch statement.
*Continue: Skips the current iteration of the loop.*/










